Create schema CTS;

DROP TABLE IF EXISTS CTS.USER;

CREATE TABLE CTS.USER
(FIRST_NAME VARCHAR2(50),
LAST_NAME VARCHAR2(50),
CUSTOMER_ID NUMBER(8),
PASSWORD VARCHAR2(20),
ACCOUNT_TYPE VARCHAR2(50),
PAN VARCHAR2(50) NOT NULL UNIQUE,
MOBILE_NUMBER NUMBER(10) UNIQUE,
EMAIL_ID VARCHAR2(50) UNIQUE,
ACCOUNT_NUMBER NUMBER(14) UNIQUE,
AMOUNT NUMBER(30,2),
PRIMARY KEY(CUSTOMER_ID));

